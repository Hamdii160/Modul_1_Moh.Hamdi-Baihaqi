# Input suhu dalam Celsius
suhu_celsius = 17

# Konversi suhu ke Fahrenheit
suhu_fahrenheit = (suhu_celsius * 9/5) + 32

# Konversi suhu ke Reamur
suhu_reamur = suhu_celsius * 4/5

# Konversi suhu ke Kelvin
suhu_kelvin = suhu_celsius + 273.15

# Menampilkan hasil konversi
print(f"Suhu {suhu_celsius}°C sama dengan {suhu_fahrenheit}°F")
print(f"Suhu {suhu_celsius}°C sama dengan {suhu_reamur}°Re")
print(f"Suhu {suhu_celsius}°C sama dengan {suhu_kelvin}K")